#!/usr/bin/env bash

### START-OF-WIDGET-ICON-MAP
function __weather_icon_map() {
  case "$1" in
  "weather_clear_day")
    icon_result=""
    ;;
  "weather_clear_night")
    icon_result=""
    ;;

  "weather_partly_cloudy_day")
    icon_result=""
    ;;
  "weather_partly_cloudy_night")
    icon_result=""
    ;;

  "weather_cloudy_day")
    icon_result=""
    ;;
  "weather_cloudy_night")
    icon_result=""
    ;;

  "weather_fog_day")
    icon_result=""
    ;;
  "weather_fog_night")
    icon_result=""
    ;;

  "weather_rain_day")
    icon_result=""
    ;;
  "weather_rain_night")
    icon_result=""
    ;;

  "weather_snow_day")
    icon_result=""
    ;;
  "weather_snow_night")
    icon_result=""
    ;;

  "weather_hail_day")
    icon_result=""
    ;;
  "weather_hail_night")
    icon_result=""
    ;;

  "weather_sleet_day")
    icon_result=""
    ;;
  "weather_sleet_night")
    icon_result=""
    ;;

  "weather_thunderstorm_day")
    icon_result=""
    ;;
  "weather_thunderstorm_night")
    icon_result=""
    ;;

  "weather_default") icon_result="" ;;
  *)
    icon_result=""
    ;;
  esac
}
### END-OF-WIDGET-ICON-MAP
